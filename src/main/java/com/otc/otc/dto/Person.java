package com.otc.otc.dto;


public class Person {
    private String name;
    private int age;

    // 생성자, 게터, 세터 생략

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}


